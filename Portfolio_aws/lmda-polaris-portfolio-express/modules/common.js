com.httpRespMsg = (status, message) => {
  const _status = status || "success";
  const _message = message || "";

  // 성공 메시지
  if (_status === "S") {
    return {
      message: _message,
      statusCode: httpStatus.OK,
    };
  } else if (_status === "F") {
    return {
      message: _message,
      statusCode: httpStatus.OK,
    };
  }
};

